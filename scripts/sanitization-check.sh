#!/usr/bin/env bash
# в”Ђв”Ђв”Ђ SNP-Customer Sanitization Audit в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# Checks all source files for leaked internal details that should NOT appear
# in the customer-facing tool.
#
# Usage: bash scripts/sanitization-check.sh
# Exit code: 0 = clean, 1 = violations found

set -euo pipefail

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo "в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ"
echo " SNP-Customer Sanitization Audit"
echo "в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ"
echo ""

VIOLATIONS=0
SEARCH_DIR="src/"

# в”Ђв”Ђ Vendor Part Numbers в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
VENDOR_PNS=(
  "VSC8504"
  "VM1502"
  "Virtium"
  "Microchip"
  "Marvell"
  "Xilinx"
  "Lattice"
  "TI-TPS"
  "LTC3"
  "ADP5"
)

echo -e "${YELLOW}Checking vendor part numbers...${NC}"
for pn in "${VENDOR_PNS[@]}"; do
  MATCHES=$(grep -rn --include='*.ts' --include='*.tsx' "$pn" "$SEARCH_DIR" 2>/dev/null || true)
  if [ -n "$MATCHES" ]; then
    echo -e "  ${RED}вњ— Found '$pn':${NC}"
    echo "$MATCHES" | sed 's/^/    /'
    VIOLATIONS=$((VIOLATIONS + 1))
  fi
done

# в”Ђв”Ђ Customer Names в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
CUSTOMER_NAMES=(
  "customer-a-pleo"
  "customer-b-pleo"
  "customer-c-pleo"
  "fms-irad"
  "customerName.*ABE"
  "customerName.*J2"
  "customerName.*JL"
  "customerName.*FMS"
)

echo -e "${YELLOW}Checking customer names...${NC}"
for name in "${CUSTOMER_NAMES[@]}"; do
  MATCHES=$(grep -rn --include='*.ts' --include='*.tsx' "$name" "$SEARCH_DIR" 2>/dev/null || true)
  if [ -n "$MATCHES" ]; then
    echo -e "  ${RED}вњ— Found $name:${NC}"
    echo "$MATCHES" | sed 's/^/    /'
    VIOLATIONS=$((VIOLATIONS + 1))
  fi
done

# в”Ђв”Ђ Internal Document Numbers в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
INTERNAL_DOCS=(
  "SNP-HW-"
  "SNP-ICD-"
  "SNP-IDD-"
  "SNP-SUM-"
  "ECO-"
  "ECN-"
  "DCN-"
)

echo -e "${YELLOW}Checking internal document numbers...${NC}"
for doc in "${INTERNAL_DOCS[@]}"; do
  MATCHES=$(grep -rn --include='*.ts' --include='*.tsx' "$doc" "$SEARCH_DIR" 2>/dev/null || true)
  if [ -n "$MATCHES" ]; then
    echo -e "  ${RED}вњ— Found '$doc':${NC}"
    echo "$MATCHES" | sed 's/^/    /'
    VIOLATIONS=$((VIOLATIONS + 1))
  fi
done

# в”Ђв”Ђ Internal Fault Codes в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "${YELLOW}Checking internal fault codes...${NC}"
MATCHES=$(grep -rn --include='*.ts' --include='*.tsx' 'ERR_0x' "$SEARCH_DIR" 2>/dev/null || true)
if [ -n "$MATCHES" ]; then
  echo -e "  ${RED}вњ— Found internal fault codes:${NC}"
  echo "$MATCHES" | sed 's/^/    /'
  VIOLATIONS=$((VIOLATIONS + 1))
fi

# в”Ђв”Ђ Internal Manual References в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "${YELLOW}Checking manual section references (ICD/IDD/SUM Sec)...${NC}"
MATCHES=$(grep -rn --include='*.ts' --include='*.tsx' -E '(ICD|IDD|SUM) - Sec' "$SEARCH_DIR" 2>/dev/null || true)
if [ -n "$MATCHES" ]; then
  echo -e "  ${RED}вњ— Found internal manual references:${NC}"
  echo "$MATCHES" | sed 's/^/    /'
  VIOLATIONS=$((VIOLATIONS + 1))
fi

# в”Ђв”Ђ GDMS-specific Environment Variables в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "${YELLOW}Checking for GDMS-specific references...${NC}"
MATCHES=$(grep -rn --include='*.ts' --include='*.tsx' 'GDMS' "$SEARCH_DIR" 2>/dev/null || true)
if [ -n "$MATCHES" ]; then
  echo -e "  ${RED}вњ— Found GDMS references:${NC}"
  echo "$MATCHES" | sed 's/^/    /'
  VIOLATIONS=$((VIOLATIONS + 1))
fi

# в”Ђв”Ђ Results в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ"
if [ $VIOLATIONS -eq 0 ]; then
  echo -e "  ${GREEN}вњ“ CLEAN вЂ” No sanitization violations found.${NC}"
  echo "в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ"
  exit 0
else
  echo -e "  ${RED}вњ— FAILED вЂ” $VIOLATIONS violation(s) found.${NC}"
  echo -e "  ${RED}  Fix before customer demo!${NC}"
  echo "в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ"
  exit 1
fi
